package kr.co.vo;

public class EatVO {
	private int e_num;
	private String e_name;
	private int e_price;
	private String e_period;
	private String e_content;
	private String e_addr;
	private String e_menu;
	private int s_num;
		
	public EatVO() {}

	public int getE_num() {
		return e_num;
	}

	public void setE_num(int e_num) {
		this.e_num = e_num;
	}

	public String getE_name() {
		return e_name;
	}

	public void setE_name(String e_name) {
		this.e_name = e_name;
	}

	public int getE_price() {
		return e_price;
	}

	public void setE_price(int e_price) {
		this.e_price = e_price;
	}

	public String getE_period() {
		return e_period;
	}

	public void setE_period(String e_period) {
		this.e_period = e_period;
	}

	public String getE_content() {
		return e_content;
	}

	public void setE_content(String e_content) {
		this.e_content = e_content;
	}

	public String getE_addr() {
		return e_addr;
	}

	public void setE_addr(String e_addr) {
		this.e_addr = e_addr;
	}
	
	public String getE_menu() {
		return e_menu;
	}

	public void setE_menu(String e_menu) {
		this.e_menu = e_menu;
	}
	
	public int getS_num() {
		return s_num;
	}

	public void setS_num(int s_num) {
		this.s_num = s_num;
	}

	

	
}
