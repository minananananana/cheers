package kr.co.vo;

public class StoreVO {
	
	private int s_num;
	private String s_name;
	private int s_zipcode;
	private String s_addr;
	private String s_detail_addr;
	private String s_tel;
	
	public int getS_num() {
		return s_num;
	}
	public void setS_num(int s_num) {
		this.s_num = s_num;
	}
	public String getS_name() {
		return s_name;
	}
	public void setS_name(String s_name) {
		this.s_name = s_name;
	}
	public int getS_zipcode() {
		return s_zipcode;
	}
	public void setS_zipcode(int s_zipcode) {
		this.s_zipcode = s_zipcode;
	}
	public String getS_addr() {
		return s_addr;
	}
	public void setS_addr(String s_addr) {
		this.s_addr = s_addr;
	}
	public String getS_detail_addr() {
		return s_detail_addr;
	}
	public void setS_detail_addr(String s_detail_addr) {
		this.s_detail_addr = s_detail_addr;
	}
	public String getS_tel() {
		return s_tel;
	}
	public void setS_tel(String s_tel) {
		this.s_tel = s_tel;
	}
	
	
}//class-end